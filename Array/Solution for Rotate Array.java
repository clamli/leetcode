class Solution {
	public void rotate(int[] nums, int k) {
		int size = nums.length;
		reverse(nums, 0, size - 1);
		reverse(nums, 0, k%size - 1);
		reverse(nums, k%size, size - 1);
    }
	
	private void reverse(int[] nums, int start, int end) {
		while (start < end) {
			int tmp = nums[start];
			nums[start] = nums[end];
			nums[end] = tmp;
			start++;
			end--;
		}
	}
}
